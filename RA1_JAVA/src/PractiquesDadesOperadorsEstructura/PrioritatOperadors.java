public class PrioritatOperadors {
    public static void main(String[] args) {
        int edat = 21;
        System.out.println(edat+6/3);
        System.out.println((edat+6)/3);
        System.out.println(2+edat>>1);
        System.out.println(2+(edat>>1));
        System.out.println(1+edat*2);
        System.out.println(edat%5+3);
        System.out.println(edat%(5+3));
        System.out.println(edat/3*2);
        System.out.println(edat/(3*2));
        System.out.println(edat<20|edat>25);

    }
}
